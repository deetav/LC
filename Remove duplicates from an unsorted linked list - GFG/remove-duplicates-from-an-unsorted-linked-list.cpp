class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
     // your code goes here
     Node* cur = head;
     Node* prev = cur;
     unordered_map<int, bool> vis;
     while(cur != nullptr) {
         if(!vis[cur->data]) {
             vis[cur->data] = true;
             prev = cur;
             cur = cur->next;
         } else {
             Node* del = prev->next;
             cur = cur->next;
             prev->next = cur;
             delete(del);
             
         }
     }
     return head;
    }
};


